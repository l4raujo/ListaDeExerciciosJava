package LacosRepeticaoDoWhile;

public class Exercicio51 {
    public static void main(String[] args) {
        //51) Faça um programa que exiba os números de 1 a 10 usando o loop "do-while".

        int contador = 1;

        do {
            System.out.println(contador);
            contador++;
        } while (contador <= 10);
    }
}
